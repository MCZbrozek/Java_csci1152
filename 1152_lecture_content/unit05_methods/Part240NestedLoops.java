import java.util.Scanner;

public class Part240NestedLoops
{
	public static void main(String[] args)
	{
		for (int i = 1; i <= 10; i++)
		{
			for (int j=1; j<=5; j++)
			{
				System.out.print('@');
			}
			System.out.println();
		} 
	} // end main
} // end class
